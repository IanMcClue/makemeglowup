import os
import streamlit as st
import requests
from openai import OpenAI
from io import BytesIO
from PIL import Image

# Initialize OpenAI client and set the API key
openai_api_key = st.secrets["OPENAI_API_KEY"]
client = OpenAI(api_key=openai_api_key)

def test_openai_api():
    try:
        # Make a simple request to the API to check if it's working
        response = client.list_engines()  # Use the correct method to test the API
        st.write("OpenAI API initialized successfully!")
    except Exception as e:
        st.write("Error initializing OpenAI API:", e)

def generate_image(client, prompt):
    response = client.image.create(
        prompt=prompt,
        n=1,
        size="1024x1024"
    )
    image_url = response['data'][0]['url']
    return image_url

def main():
    st.title("DALL-E Image Generator")
    
    # Test OpenAI API initialization
    test_openai_api()
    
    prompt = st.text_input("Enter a description for the image:")
    
    if st.button("Generate Image"):
        with st.spinner("Generating image..."):
            image_url = generate_image(client, prompt)
            response = requests.get(image_url)
            img = Image.open(BytesIO(response.content))
            st.image(img, caption="Generated by DALL-E", use_column_width=True)

if __name__ == "__main__":
    main()