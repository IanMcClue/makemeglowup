import streamlit as st
import requests
from openai import OpenAI
from io import BytesIO
from PIL import Image

# Initialize OpenAI client and set the API key
client = OpenAI(api_key=st.secrets["OPENAI_API_KEY"])

def test_openai_api():
    try:
        # Make a simple request to the API
        response = client.Usage.retrieve()
        st.write("OpenAI API initialized successfully!")
    except Exception as e:
        st.write("Error initializing OpenAI API:", e)

def generate_image(client, model_choice, prompt):
    response = client.Image.generate(
        model=model_choice,
        prompt=prompt,
        size="1024x1024",
        quality="standard",
        n=1
    )
    image_url = response.data[0].url
    return image_url

def main():
    st.title("DALL-E Image Generator")
    
    model_choice = "image-alpha-001"  # You can update this if there are different models available
    prompt = st.text_input("Enter a description for the image:")
    
    if st.button("Generate Image"):
        with st.spinner("Generating image..."):
            image_url = generate_image(client, model_choice, prompt)
            response = requests.get(image_url)
            img = Image.open(BytesIO(response.content))
            st.image(img, caption="Generated by DALL-E", use_column_width=True)

    # Test OpenAI API initialization
    test_openai_api()

if __name__ == "__main__":
    main()
